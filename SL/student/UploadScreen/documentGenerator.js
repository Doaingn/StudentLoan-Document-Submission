const calculateAge = (birthDateTimestamp) => {
    console.log(`üîç calculateAge called with:`, birthDateTimestamp);
    console.log(`üîç Type:`, typeof birthDateTimestamp);
    
    if (!birthDateTimestamp) {
        console.log(`‚ùå Birth date is null/undefined`);
        return null;
    }

    let birthDate;
    if (typeof birthDateTimestamp.toDate === 'function') {
        birthDate = birthDateTimestamp.toDate();
        console.log(`‚úÖ Converted from Firestore Timestamp:`, birthDate);
    } else if (typeof birthDateTimestamp === 'number') {
        birthDate = new Date(birthDateTimestamp);
        console.log(`‚úÖ Converted from Unix timestamp:`, birthDate);
    } else if (birthDateTimestamp instanceof Date) {
        birthDate = birthDateTimestamp;
        console.log(`‚úÖ Already Date object:`, birthDate);
    } else {
        console.warn('‚ùå Invalid birth date format:', birthDateTimestamp);
        return null;
    }

    const today = new Date();
    let age = today.getFullYear() - birthDate.getFullYear();
    const m = today.getMonth() - birthDate.getMonth();

    if (m < 0 || (m === 0 && today.getDate() < birthDate.getDate())) {
        age--;
    }
    
    console.log(`‚úÖ Calculated age: ${age} years`);
    return age;
};

